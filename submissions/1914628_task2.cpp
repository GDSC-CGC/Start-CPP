#include<iostream>
using namespace std;
int main()
{
    cout<<"Size of boolean data type : "<<sizeof(bool)<<" byte\n";
    cout<<"Size of char data type : "<<sizeof(char)<<" byte\n";
    cout<<"Size of int data type : "<<sizeof(int)<<" byte\n";
    cout<<"Size of floating point data type : "<<sizeof(float)<<" byte\n";
    cout<<"Size of double floating point data type : "<<sizeof(double)<<" byte\n";
    return 0;
}

