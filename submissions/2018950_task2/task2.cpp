#include <iostream>
using namespace std;

int main()
{
    int m;
    char a;
    double n;
    float i;
    bool b;
    cout<<"size of int : "<<sizeof(m)<<endl;
    cout<<"size of char : "<<sizeof(a)<<endl;
    cout<<"size of double : "<<sizeof(n)<<endl;
    cout<<"size of float : "<<sizeof(i)<<endl;
    cout<<"size of bool : "<<sizeof(b)<<endl;
    return 0;
}
