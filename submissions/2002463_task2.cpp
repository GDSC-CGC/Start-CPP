#include <iostream>
using namespace std;

int main() {
    int a;
    char ch;
    double d;
    short s;
    long l;

    cout << "\nSize of int: " << sizeof(a);
    cout << "\nSize of char: " << sizeof(ch);
    cout << "\nSize of double: " << sizeof(d);
    cout << "\nSize of short: " << sizeof(s);
    cout << "\nSize of long: " << sizeof(l); 
    return 0;
}
