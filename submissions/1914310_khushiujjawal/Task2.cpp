#include <iostream>
using namespace std;
int main()
{
   cout << "Size of char data type : " << sizeof(char) << " byte" << endl;
   cout << "Size of int data type : " << sizeof(int) << " byte" << endl;
   cout << "Size of unsigned int data type : " << sizeof(unsigned int) << " byte" << endl;
   cout << "Size of floating point data type : " << sizeof(float) << " byte" << endl;
   cout << "Size of double floating point data type : " << sizeof(double) << " byte" << endl;
   return 0;
}