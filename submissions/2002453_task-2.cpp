#include <iostream>

using namespace std;

int main()
{
    cout<<"Size of int data type is: "<<sizeof(int)<<endl;
    cout<<"Size of float data type is: "<<sizeof(float)<<endl;
    cout<<"Size of boolean data type is: "<<sizeof(bool)<<endl;
    cout<<"Size of character data type is: "<<sizeof(char)<<endl;
    cout<<"Size of double floating data type is: "<<sizeof(double)<<endl;
    
    return 0;
}
